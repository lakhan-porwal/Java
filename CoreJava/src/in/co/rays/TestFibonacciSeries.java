package in.co.rays;

public class TestFibonacciSeries {

	public static void main(String[] args) {

		int a = 0;
		int b = 1;

		System.out.println(a);
		System.out.println(b);

		int c = a + b;
		System.out.println(c);

		int d = c + b;
		System.out.println(d);

		int e = d + c;
		System.out.println(e);

	}
}
