package in.co.rays;

public class Switch {
	public static void main(String[] args) {
		
		int num = 10;
		switch (num) {
		case 1 :
			System.out.println("one");
		case 2 : 
			System.out.println("two");
		case 3 :
			System.out.println("Four");
		default :
		    System.out.println("Ten");
		}
	}

}
